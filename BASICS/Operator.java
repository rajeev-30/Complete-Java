package BASICS;

public class Operator {
    public static void main(String[] args) {
        int num1 = 12,num2 = 8;
        System.out.println(num1 + num2); // Arithmetic operator
        System.out.println(num1 - num2); // Arithmetic operator
        System.out.println(num1 * num2); // Arithmetic operator
        System.out.println(num1 / num2); // Arithmetic operator
        System.out.println(num1 % num2); // Arithmetic operator
        System.out.println(num1++);      // Arithmetic operator
        System.out.println(++num1);      // Arithmetic operator
        System.out.println(num1--);     // Arithmetic operator
        System.out.println(--num1);     // Arithmetic operator



        //assignment operator ---> " =, +=, -=, /=, *=, %= "

       

        /*
        comparison operator ---> 
        1. == : checks for equality of two value 
        2. != : checks if two values are not equal
        3. >
        4. <
        5. >=
        6. <=
        */



        /* 
        Logical operator --->
        1. && : Logical and operator
        2. || : Logical or operator
        3. !  : Logical not
        */

    }
    
}
