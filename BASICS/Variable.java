package BASICS;

public class Variable {
    public static void main(String[] args) {

        String name = "rajeev";
        int a = 5;
        float b = 5.5f; // "f" is for float
        boolean y = true; // boolean takes only "true & false";
        char c = 'A';
        double d = 455.32523535d; // "d" is for double

        System.out.println("your name is: " + name);

        System.out.println("int is: " + a);
        System.out.println("float is: " + b);
        System.out.println("boolean is: " + y);

    }

}
